

public class Register {
    int id;
    Integer num;

    public Register(int id,Integer num){
        this.id = id;
        this.num = num;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public int getId() {
        return id;
    }

    public Integer getNum() {
        return num;
    }
}
